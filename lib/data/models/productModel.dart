import 'package:staples/domain/entities/product.dart';

class ProductModel extends Product {
  const ProductModel({
    required final int gtin,
    final String producerIconPath = '',
    final String producerIconAlt = '',
    final String imagePath = '',
    final String imageAlt = '',
    final String name = '',
    final String slogan = '',
    final String qualityIconPath = '',
    final String qualityIconAlt = '',
    final String description = '',
    final String nameDescription = '',
    final String coo = '',
    // TODO final List<Ingredient> ingredients = '',
    final String ingredients = '',
    final String ingredientsLegende = '',
    // TODO final List<NutritionalValue> nutritionalValues = '',
    final String legalStatus = '',
    final String peculiarity = '',
    final String storageHint = '',
    final String periodOfUsage = '',
    final bool cooVariable = false,
    final String cooMain = '',
    final bool organic = false,
    final String organicCertification = '',
    final String organicCertificationAddition = '',
    final String organicBody = '',
    final String standard = '',
    final String distributor = '',
    final String additives = '',
    final bool vegan = false,
    final bool vegetarian = false,
    final bool unsweetened = false,
    final bool raw = false,
    final String productKey = '',
    // TODO final AllergyInformation allergy;
    final String allergy = '',
    final double rating = 0,
  }) : super(
          gtin: gtin,
          producerIconPath: producerIconPath,
          producerIconAlt: producerIconAlt,
          imagePath: imagePath,
          imageAlt: imageAlt,
          name: name,
          slogan: slogan,
          qualityIconPath: qualityIconPath,
          qualityIconAlt: qualityIconAlt,
          description: description,
          nameDescription: nameDescription,
          coo: coo,
          ingredients: ingredients,
          ingredientsLegende: ingredientsLegende,
          legalStatus: legalStatus,
          peculiarity: peculiarity,
          storageHint: storageHint,
          periodOfUsage: periodOfUsage,
          cooVariable: cooVariable,
          cooMain: cooMain,
          organic: organic,
          organicCertification: organicCertification,
          organicCertificationAddition: organicCertificationAddition,
          organicBody: organicBody,
          standard: standard,
          distributor: distributor,
          additives: additives,
          vegan: vegan,
          vegetarian: vegetarian,
          unsweetened: unsweetened,
          raw: raw,
          productKey: productKey,
          allergy: allergy,
          rating: rating,
        );

  factory ProductModel.fromJson(Map<String, dynamic> map) {
    return ProductModel(
      gtin: map['gtin'] as int,
    );
  }
}
